#include <stdio.h>
#include <stdlib.h>

/**
 * reset_to_98 - Met à jour la valeur du pointeur d'un entier à 98.
 * @n: Pointeur vers l'entier à mettre à jour.
 *
 * Description: Cette fonction prend un pointeur vers un entier en argument
 *              et met à jour la valeur pointée à 98.
 */
void reset_to_98(int *n)
{
	*n = 98;
}

